#include "pybind11/pybind11.h"
#include "pybind11/stl.h"
#include "shared_struct.hpp"

namespace py = pybind11;

PYBIND11_MODULE(example, m) {
    py::class_<Owner>(m, "Owner")
        .def(py::init<>())
        .def_readwrite("name", &Owner::name)
        .def_readwrite("age", &Owner::age)
        .def("get_name", &Owner::get_name)
        .def("set_name", &Owner::set_name)
        .def("get_age", &Owner::get_age)
        .def("set_age", &Owner::set_age);

    py::class_<Pet>(m, "Pet")
        .def(py::init<>())
        .def_readwrite("own", &Pet::own)
        .def_readwrite("arr_own", &Pet::arr_own)
        .def_readwrite("vec_own", &Pet::vec_own)
        .def_readwrite("d", &Pet::d)
        .def_readwrite("f", &Pet::f)
        .def_readwrite("i32", &Pet::i32)
        .def_readwrite("i64", &Pet::i64)
        .def_readwrite("u32", &Pet::u32)
        .def_readwrite("u64", &Pet::u64)
        .def_readwrite("bval", &Pet::bval)
        .def_readwrite("s", &Pet::s)
        .def_readwrite("arr_d", &Pet::arr_d)
        .def_readwrite("arr_f", &Pet::arr_f)
        .def_readwrite("arr_i32", &Pet::arr_i32)
        .def_readwrite("arr_i64", &Pet::arr_i64)
        .def_readwrite("arr_u32", &Pet::arr_u32)
        .def_readwrite("arr_u64", &Pet::arr_u64)
        .def_readwrite("arr_bval", &Pet::arr_bval)
        .def_readwrite("arr_s", &Pet::arr_s)
        .def_readwrite("vec_d", &Pet::vec_d)
        .def_readwrite("vec_f", &Pet::vec_f)
        .def_readwrite("vec_i32", &Pet::vec_i32)
        .def_readwrite("vec_i64", &Pet::vec_i64)
        .def_readwrite("vec_u32", &Pet::vec_u32)
        .def_readwrite("vec_u64", &Pet::vec_u64)
        .def_readwrite("vec_bval", &Pet::vec_bval)
        .def_readwrite("vec_s", &Pet::vec_s)
        .def("bark", &Pet::bark)
        .def("freeze", [](const Pet& pet) { return py::bytes(pet.freeze()); })
        .def("melt", [](Pet& pet, const py::bytes& src) {
            pet.melt(std::string(src));
        })
        .def("get_own", &Pet::get_own)
        .def("set_own", &Pet::set_own)
        .def("get_arr_own", &Pet::get_arr_own, py::return_value_policy::reference_internal)
        .def("set_arr_own", &Pet::set_arr_own)
        .def("get_vec_own", &Pet::get_vec_own, py::return_value_policy::reference_internal)
        .def("set_vec_own", &Pet::set_vec_own)
        .def("get_d", &Pet::get_d)
        .def("set_d", &Pet::set_d)
        .def("get_f", &Pet::get_f)
        .def("set_f", &Pet::set_f)
        .def("get_i32", &Pet::get_i32)
        .def("set_i32", &Pet::set_i32)
        .def("get_i64", &Pet::get_i64)
        .def("set_i64", &Pet::set_i64)
        .def("get_u32", &Pet::get_u32)
        .def("set_u32", &Pet::set_u32)
        .def("get_u64", &Pet::get_u64)
        .def("set_u64", &Pet::set_u64)
        .def("get_bval", &Pet::get_bval)
        .def("set_bval", &Pet::set_bval)
        .def("get_s", &Pet::get_s)
        .def("set_s", &Pet::set_s)
        .def("get_arr_d", &Pet::get_arr_d, py::return_value_policy::reference_internal)
        .def("set_arr_d", &Pet::set_arr_d)
        .def("get_arr_f", &Pet::get_arr_f, py::return_value_policy::reference_internal)
        .def("set_arr_f", &Pet::set_arr_f)
        .def("get_arr_i32", &Pet::get_arr_i32, py::return_value_policy::reference_internal)
        .def("set_arr_i32", &Pet::set_arr_i32)
        .def("get_arr_i64", &Pet::get_arr_i64, py::return_value_policy::reference_internal)
        .def("set_arr_i64", &Pet::set_arr_i64)
        .def("get_arr_u32", &Pet::get_arr_u32, py::return_value_policy::reference_internal)
        .def("set_arr_u32", &Pet::set_arr_u32)
        .def("get_arr_u64", &Pet::get_arr_u64, py::return_value_policy::reference_internal)
        .def("set_arr_u64", &Pet::set_arr_u64)
        .def("get_arr_bval", &Pet::get_arr_bval, py::return_value_policy::reference_internal)
        .def("set_arr_bval", &Pet::set_arr_bval)
        .def("get_arr_s", &Pet::get_arr_s, py::return_value_policy::reference_internal)
        .def("set_arr_s", &Pet::set_arr_s)
        .def("get_vec_d", &Pet::get_vec_d, py::return_value_policy::reference_internal)
        .def("set_vec_d", &Pet::set_vec_d)
        .def("get_vec_f", &Pet::get_vec_f, py::return_value_policy::reference_internal)
        .def("set_vec_f", &Pet::set_vec_f)
        .def("get_vec_i32", &Pet::get_vec_i32, py::return_value_policy::reference_internal)
        .def("set_vec_i32", &Pet::set_vec_i32)
        .def("get_vec_i64", &Pet::get_vec_i64, py::return_value_policy::reference_internal)
        .def("set_vec_i64", &Pet::set_vec_i64)
        .def("get_vec_u32", &Pet::get_vec_u32, py::return_value_policy::reference_internal)
        .def("set_vec_u32", &Pet::set_vec_u32)
        .def("get_vec_u64", &Pet::get_vec_u64, py::return_value_policy::reference_internal)
        .def("set_vec_u64", &Pet::set_vec_u64)
        .def("get_vec_bval", &Pet::get_vec_bval, py::return_value_policy::reference_internal)
        .def("set_vec_bval", &Pet::set_vec_bval)
        .def("get_vec_s", &Pet::get_vec_s, py::return_value_policy::reference_internal)
        .def("set_vec_s", &Pet::set_vec_s);
}